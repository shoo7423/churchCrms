                Our History
Evangelistic Assemblies of God (EAGT) was registered on 21/10/1981 under the Societies Ordinance of 1954 and given registration number SO. 6246.

Evangelistic Assemblies of God is a church that arose from a great revival that began in Azusa, 
California in the United States, at the beginning of the 20th century. Paul Deer, who initially came as an independent missionary,
 founded the Pentecost Holiness Association (PHA) Church in 1928 where The first service of the Church started early in 1929 in Igale in Mbalizi District in Mbeya region. 
 In 1938, Derr returned to the United States and the following year, 1939, he joined the Assemblies of God Church,
  thus entrusting the work that had been established in Tanzania (then called Tanganyika) to the hands of the General Council of the Assemblies of God in the United States.
   And ask the leadership to send a Missionary to the work to raise it while he is on vacation.

History shows us that in the early years the church was managed by American missionaries together 
with African pastors at that time (Yohana Mpayo [Deceased] and Petros) who were bishops under the Assemblies of God Mission. 
The church continued to be led in that system until 1967. 
The very first TAG church was in Igale, Mbalizi district, Mbeya region. It was the year 1967, 
the leadership of the Assemblies of God Mission, decided to hand over the church to the leadership of the locals and Pastor Moses kulola,
 was elected as the first Tanzanian Archbishop to lead the  Evangelistic Assemblies of God (EAGT).
 This church entered this country as a result of the great Pentecostal revival that happened in Azuza in the United States.
